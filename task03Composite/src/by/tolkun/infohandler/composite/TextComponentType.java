package by.tolkun.infohandler.composite;

/**
 * Class to present type of {@code TextComponent}.
 *
 * @author Kirill Tolkun
 */
public enum TextComponentType {
    /**
     * type of text.
     */
    TEXT,
    /**
     * Type of paragraph.
     */
    PARAGRAPH,
    /**
     * Type of sentence.
     */
    SENTENCE,
    /**
     * Type of lexeme.
     */
    LEXEME,
    /**
     * Type of word.
     */
    WORD,
    /**
     * Type of expression.
     */
    EXPRESSION,
    /**
     * Type of punctuation.
     */
    PUNCTUATION,
    /**
     * Type of symbol.
     */
    SYMBOL
}
